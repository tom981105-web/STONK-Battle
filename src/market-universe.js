function makeName(rng, sector, used) {
  for (let attempt = 0; attempt < 40; attempt++) {
    const prefix = pick(rng, COMMON_PREFIX);
    const useKw = sector.kw.length && chance(rng, 0.55);
    const kw = useKw ? pick(rng, sector.kw) : "";
    const sfx = pick(rng, sector.sfx.length ? sector.sfx : DEF_SFX);
    const name = prefix + kw + sfx;
    if (name.length <= 11 && !used.has(name)) { used.add(name); return name; }
  }
  // fallback: 숫자 접미로 유일성 보장
  let n = 2;
  let base = pick(rng, COMMON_PREFIX) + pick(rng, sector.sfx.length ? sector.sfx : DEF_SFX);
  while (used.has(base + n)) n++;
  const name = base + n;
  used.add(name);
  return name;
}

function tickerFromName(rng, name) {
  const A = "ABCDEFGHJKLMNPQRSTUVWXYZ";
  let t = "";
  for (let i = 0; i < 4; i++) t += A[Math.floor(rng() * A.length)];
  return t;
}

