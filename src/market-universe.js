  return `${name}은(는) ${sector.name} 업종의 가상 기업으로, ${region} 기반으로 사업을 전개합니다. ` +
    `${strength}이 강점이지만 ${weakness}에는 유의해야 합니다. 모든 데이터는 가상입니다.`;
