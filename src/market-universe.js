// 목적: 업종 85개+ / 업종당 100개+ 가상 회사명을 "단어 풀 + 생성 규칙 + seed PRNG"로
//       즉석 생성한다. 전체 풀을 저장하지 않고, 방에서 선택된 종목만 game.js 가
//       Firebase 에 저장한다. (성능/용량 보호)
//
// 결정성: Math.random 미사용. FNV-1a 해시 + mulberry32 PRNG.
//   같은 seed(=roomCode) → 항상 같은 회사 구성. 다른 seed → 다른 구성.
//
// game.js 는 이 모듈의 spec(이름/업종/타입/역할/기준가/변동성/설명/태그)을 받아
// 기존 makeStock 으로 battle 스키마 종목 객체를 만든다. (marketTick 로직 불변)

// ===== PRNG =====
export function fnv1a(str) {
  let h = 0x811c9dc5;
  const s = String(str);
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i);
    h = Math.imul(h, 0x01000193);
  }
  return h >>> 0;
}

export function mulberry32(a) {
  return function () {
    a |= 0;
    a = (a + 0x6d2b79f5) | 0;
    let t = Math.imul(a ^ (a >>> 15), 1 | a);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

export function rngFromSeed(seed) {
  return mulberry32(fnv1a(seed));
}

function pick(rng, arr) { return arr[Math.floor(rng() * arr.length) % arr.length]; }
function rint(rng, min, max) { return Math.floor(rng() * (max - min + 1)) + min; }
function rfloat(rng, min, max) { return rng() * (max - min) + min; }
function chance(rng, p) { return rng() < p; }

