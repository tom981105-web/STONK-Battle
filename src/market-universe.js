// 한 회사 spec 생성 (equity 또는 special)
function buildCompany(rng, sector, used, roleHint) {
  const name = makeName(rng, sector, used);
  const basePrice = Math.round(rfloat(rng, sector.base[0], sector.base[1]) / 10) * 10;
  const volatility = Math.round(rfloat(rng, sector.volat[0], sector.volat[1]));
  const trend = +(rfloat(rng, -0.4, 0.4)).toFixed(3);
  const marketCap = basePrice * rint(rng, 200000, 9000000);
  return {
    name,
    sector: sector.name,
    type: sector.special || "stock",
    role: roleHint || "",
    basePrice,
    marketCap,
    volatility,
    trend,
    risk: sector.risk,
    ticker: tickerFromName(rng, name),
    foundedYear: rint(rng, 1985, 2024),
    region: pick(rng, REGIONS),
    employees: rint(rng, 30, 12000),
    product: pick(rng, FOUND_PRODUCTS.default),
    strength: pick(rng, STRENGTHS),
    weakness: pick(rng, WEAKNESSES),
    newsTone: pick(rng, NEWS_TONE),
    tags: sector.tags.slice(0, 4),
    description: describe(rng, name, sector),
  };
}

// ===== 방 카탈로그 생성 (game.js 가 호출) =====
// seed(roomCode) 기반으로 다양한 업종에 분산된 equity 회사 + 약간의 특수자산을 만든다.
// 반환: company spec 배열. game.js 가 makeStock 으로 battle 종목 객체화.
export function buildRoomCatalog(seed, opts = {}) {
  const rng = rngFromSeed("catalog:" + seed);
  const target = opts.count || rint(rng, 48, 72);
  const used = new Set();
  const out = [];

  // 1) 업종 클러스터: 8~12개 업종을 골라 각 업종에 대장주1 + 부대장주1~2 + 관련/일반 다수
  const sectorCount = Math.min(EQUITY_SECTORS.length, rint(rng, 9, 13));
  const chosen = [];
  const pool = EQUITY_SECTORS.slice();
  for (let i = 0; i < sectorCount && pool.length; i++) {
    const idx = Math.floor(rng() * pool.length);
    chosen.push(pool.splice(idx, 1)[0]);
  }
  chosen.forEach((sector) => {
    out.push(buildCompany(rng, sector, used, "leader"));
    const subs = rint(rng, 1, 2);
    for (let i = 0; i < subs; i++) out.push(buildCompany(rng, sector, used, "sub"));
    const related = rint(rng, 2, 4);
    for (let i = 0; i < related; i++) out.push(buildCompany(rng, sector, used, "related"));
    const normal = rint(rng, 0, 2);
    for (let i = 0; i < normal; i++) out.push(buildCompany(rng, sector, used, "normal"));
  });

  // 2) 특수자산 일부 포함 (ETF/채권/원자재/리츠/SPAC/우선주 등)
  SPECIAL_SECTORS.forEach((sector) => {
    const n = sector.special === "preferred" ? rint(rng, 0, 2) : rint(rng, 1, 2);
    for (let i = 0; i < n; i++) out.push(buildCompany(rng, sector, used, ""));
  });

  // 3) 목표 개수까지 equity 추가/축소
  while (out.length < target) {
    const sector = pick(rng, EQUITY_SECTORS);
    out.push(buildCompany(rng, sector, used, "related"));
  }
  return out.slice(0, Math.max(target, 24));
}

// ===== IPO 회사 1건 생성 (game.js processIpo 가 호출) =====
// usedNames: 현재 방에 이미 있는 회사명 Set (중복 방지)
export function buildIpoCompany(seed, usedNames) {
  const rng = rngFromSeed("ipo:" + seed);
  const used = usedNames instanceof Set ? usedNames : new Set(usedNames || []);
  // IPO 는 성장/테마/고위험 업종 위주
  const growth = EQUITY_SECTORS.filter((s) => s.risk === "높음" || s.risk === "매우 높음");
  const sector = pick(rng, growth.length ? growth : EQUITY_SECTORS);
  const c = buildCompany(rng, sector, used, "normal");
  c.ipoProfile = { hot: chance(rng, 0.5), expectedBand: [Math.round(c.basePrice * 0.9), Math.round(c.basePrice * 1.6)] };
  c.volatility = Math.min(98, c.volatility + 10);
  return c;
}

